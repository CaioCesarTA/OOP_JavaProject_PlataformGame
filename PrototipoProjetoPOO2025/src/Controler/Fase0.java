    public boolean isSolido(float x, float y){
        if(x<0 || x>Consts.RES.width) return true;
        if(y<0 || y>Consts.RES.height) return true;
        if(infoCenario[(int)(y/Consts.CELL_SIDE)][(int)(x/Consts.CELL_SIDE)] != 13) return true;

        return false;
    }
    
